package bean.example;
public class Teacher
{
  private String name;
  private int age;
  private int pay;
  public void setName(String name)
  {
  this.name=name;
  }
  public void setAge(int age)
  {
  this.age=age;
  }
  public void setPay(int pay)
  {
  this.pay=pay;
  }
 
  public String getName() {
    return name;
  }
  public int getAge() 
  {
  return age;
  }
  public int getPay() 
  {
  return pay;
  }
}
